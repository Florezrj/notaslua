-- 1) Nomes de variáveis não podem ter acentos ou caracteres especiais.
-- 2) Nomes de variáveis não podem ter espaços
-- 3) As variavéis são sensiveis a CapsLock, então se tiver diferente uma letra, fará diferença!
-- 4) Nomes de variáveis não podem ser iguais as palavras reservadas pela linguagem Ex:
-- false, true, print, if, then, else, end, etc...

-- O que pode ter? Letras, Números, Underscore "_"


